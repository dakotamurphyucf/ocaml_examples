let get_longest_sub_array arr =
  let sub_hash = Hashtbl.create (module Int) in
  let longest = ref 0 in
  Array.iteri arr ~f:(fun index n ->
      let remove_indexs = ref [] in
      Hashtbl.iteri sub_hash ~f:(fun ~key:i ~data:tbl ->
          match Hashtbl.find tbl n with
          | None ->
            Hashtbl.set tbl ~key:n ~data:true;
            let len = Hashtbl.length tbl in
            if len > !longest then longest := len
          | Some _ -> remove_indexs := i :: !remove_indexs);
      List.iter !remove_indexs ~f:(Hashtbl.remove sub_hash);
      let tbl_index = Hashtbl.create (module Int) in
      Hashtbl.set tbl_index ~key:n ~data:true;
      Hashtbl.set sub_hash ~key:index ~data:tbl_index);
  !longest
;;

