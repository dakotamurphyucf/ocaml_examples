let bfs start g =
  let v = Hashtbl.create 100 in
  (*ideally it should be the # of distinct nodes*)
  let q = Queue.create () in
  (*v is for visited nodes*)
  let rec bfs' cur_n acc =
    get_neighbors cur_n g Int.equal
    |> List.iter (fun n ->
           try Hashtbl.find v n with
           | Not_found ->
             Hashtbl.add v n ();
             Queue.push n q);
    Hashtbl.add v cur_n ();
    try bfs' (Queue.pop q) (cur_n :: acc) with
    | Queue.Empty -> List.rev (cur_n :: acc)
  in
  bfs' start []
